<?php

class User
{
    protected $id;
    protected $nom;
    protected $prenom;
    protected $email;
    protected $password;

    public function getId()
    {
        return $this->id;
    }
    public function setId($id)
    {
        $this->id = $id;
    }

    public function getNom()
    {
        return $this->nom;
    }
    public function setNom($nom)
    {
        $this->nom = $nom;
    }

}
